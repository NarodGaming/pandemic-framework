namespace AlkalineThunder.Pandemic.Skinning
{
    /// <summary>
    /// Represents all possible named skin fonts.
    /// </summary>
    public enum SkinFontStyle
    {
        /// <summary>
        /// Specifies the default paragraph font.
        /// </summary>
        Paragraph,
        
        /// <summary>
        /// Specifies the top-level heading font.
        /// </summary>
        Heading1,
        
        /// <summary>
        /// Specifies the second-level heading font.
        /// </summary>
        Heading2,
        
        /// <summary>
        /// Specifies the third-level heading font.
        /// </summary>
        Heading3,
        
        /// <summary>
        /// Specifies the overline heading font.
        /// </summary>
        Overline,
        
        /// <summary>
        /// Specifies the font used for code and the Terminal.
        /// </summary>
        Code,
        
        /// <summary>
        /// Specifies the font used for Feed text.
        /// </summary>
        FeedBody,
        
        /// <summary>
        /// Specifies the feed username font.
        /// </summary>
        FeedUsername,
        
        /// <summary>
        /// Specifies the font used for inputs.
        /// </summary>
        Input,
        
        /// <summary>
        /// Specifies the font used for list items.
        /// </summary>
        ListItem,
        
        /// <summary>
        /// Specifies the font used for gateway panel titles.
        /// </summary>
        PanelTitle,
        
        /// <summary>
        /// Specifies the font used for the Gateway system bar.
        /// </summary>
        Systembar
    }
}