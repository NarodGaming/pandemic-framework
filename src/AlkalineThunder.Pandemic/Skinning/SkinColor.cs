namespace AlkalineThunder.Pandemic.Skinning
{
    /// <summary>
    /// Represents all possible named GUI skin colors.
    /// </summary>
    public enum SkinColor 
    {
        /// <summary>
        /// Specifies the default background color.
        /// </summary>
        Default,
        
        /// <summary>
        /// Specifies the primary accent color.
        /// </summary>
        Primary,
        
        /// <summary>
        /// Specifies the secondary accent color.
        /// </summary>
        Secondary,
        
        /// <summary>
        /// Specifies the information color.
        /// </summary>
        Info,
        
        /// <summary>
        /// Specifies the color of success.
        /// </summary>
        Success,
        
        /// <summary>
        /// Specifies the error color.
        /// </summary>
        Error,
        
        /// <summary>
        /// Specifies the warning color.
        /// </summary>
        Warning,
        
        /// <summary>
        /// Specifies the default text color.
        /// </summary>
        Text,
        
        /// <summary>
        /// Specifies the color of feed authors' usernames.
        /// </summary>
        FeedUsername,
        
        /// <summary>
        /// Specifies the feed avatar background color.
        /// </summary>
        FeedAvatar,
        
        /// <summary>
        /// Specifies the feed body text color.
        /// </summary>
        FeedBody,
        
        /// <summary>
        /// Specifies the gateway panel background color.
        /// </summary>
        GatewayPanel,
        
        /// <summary>
        /// Specifies the gateway system bar background color.
        /// </summary>
        GatewaySystemBar,
        
        /// <summary>
        /// Specifies the terminal background color.
        /// </summary>
        TerminalBackground,
        
        /// <summary>
        /// Specifies the wallpaper color.
        /// </summary>
        Wallpaper,
        
        /// <summary>
        /// Specifies the gateway panel border color.
        /// </summary>
        GatewayPanelBorder,
        
        /// <summary>
        /// Specifies the terminal text color.
        /// </summary>
        TerminalForeground,
        
        /// <summary>
        /// Specifies the gateway panel title background color.
        /// </summary>
        PanelTitle,
        
        /// <summary>
        /// Specifies the gateway panel title text color.
        /// </summary>
        PanelTitleText,
        
        /// <summary>
        /// Specifies the button background color.
        /// </summary>
        Button,
        
        /// <summary>
        /// Specifies the button text color when applicable.
        /// </summary>
        ButtonText,
        
        /// <summary>
        /// Specifies the editor line number area color. 
        /// </summary>
        EditorGutter,
        
        /// <summary>
        /// Specifies the editor line number text color.
        /// </summary>
        EditorGutterText,
        
        /// <summary>
        /// Specifies the editor active line color.
        /// </summary>
        EditorHighlight,
        
        /// <summary>
        /// Specifies the editor text color.
        /// </summary>
        EditorText,
        
        /// <summary>
        /// Specifies the logo color in the Main Menu.
        /// </summary>
        LogoTint
    }
}