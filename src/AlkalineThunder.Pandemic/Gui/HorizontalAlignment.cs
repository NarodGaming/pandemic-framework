namespace AlkalineThunder.Pandemic.Gui
{
    /// <summary>
    /// Represents the way a control should be aligned horizontally in it's parent..
    /// </summary>
    public enum HorizontalAlignment
    {
        /// <summary>
        /// The control is stretched across it's parent.
        /// </summary>
        Stretch,
        
        /// <summary>
        /// The control is aligned to the left.
        /// </summary>
        Left,
        
        /// <summary>
        /// The control is aligned to the center.
        /// </summary>
        Center,
        
        /// <summary>
        /// The control is aligned to the right.
        /// </summary>
        Right
    }
}
