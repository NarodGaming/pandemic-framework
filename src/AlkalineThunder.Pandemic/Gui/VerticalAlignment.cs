namespace AlkalineThunder.Pandemic.Gui
{
    /// <summary>
    /// Represents the way in which a control shall be aligned vertically in it's parent bounds.
    /// </summary>
    public enum VerticalAlignment
    {
        /// <summary>
        /// The control stretches vertically to fill it's parsent bounds.
        /// </summary>
        Stretch,
        
        /// <summary>
        /// The control is aligned to the top of the parent bounds.
        /// </summary>
        Top,
        
        /// <summary>
        /// The control is aligned to the centre of the parent bounds.
        /// </summary>
        Center,
        
        /// <summary>
        /// The control is aligned to the bottom of the parent bounds.
        /// </summary>
        Bottom
    }
}
