namespace AlkalineThunder.Pandemic.Gui
{
    /// <summary>
    /// Defines how a line of text should be aligned.
    /// </summary>
    public enum TextAlign
    {
        /// <summary>
        /// Align the text to the start of the line.
        /// </summary>
        Left,

        /// <summary>
        /// Center the text horizontally.
        /// </summary>
        Center,

        /// <summary>
        /// Align the text to the right of the line.
        /// </summary>
        Right
    }
}
